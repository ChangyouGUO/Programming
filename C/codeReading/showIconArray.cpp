void DrawIconFromTop(int DataLen, unsigned short (*Hdmi_voice)[3]) {
  unsigned short* pRow = 0;
  unsigned short last_row = 0;
  unsigned short col = 0;

  for (int i = 0; i < DataLen; i++) {
    pRow = Hdmi_voice[i];
    if (last_row != Hdmi_voice[i][0]) {
      printf("\n");
      last_row = Hdmi_voice[i][0];
      col = 0;
    }

    while (col <= Hdmi_voice[i][2]) {
      if (col >= Hdmi_voice[i][1] && col < Hdmi_voice[i][2]) {
        printf("1");
      } else {
        printf("0");
      }
      col++;
      if (col % 10 == 0) {
        printf(" ");
      }
    }
  }
}

