void showFont(const int *array, int size)
{
    int y = 0;
    int x = 0;
    for (y; y < size; y++)
    {
        int font = array[y];
        for (x = 0; x < size; x++)
        {
            if (x % 8 == 0)
            {
                printf(" ");
            }
            if ((font & (1 << (size - 1 - x))) != 0)
            {
                printf("1 ");
            }
            else
            {
                printf("0 ");
            }
        }
        printf("\t%x", font);
        printf("\n");
    }
}

